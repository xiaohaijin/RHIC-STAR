struct LASERINO_t {
  Int_t Raft, Sector, Bundle, Mirror;
  Double_t X, Y, Z, ThetaZ, DeltaPhi; // mm
  Int_t Status; // 0 is o.k., =1 failed X, =2 Y, =3 Z, =4 Theta, =5 Phi, =6 Dead
  Double_t dX, ddX, dY, ddY;          // cm
  void    Print() const {
	  cout << Form("LASERINO:%2i,%2i,%1i,%1i,%9.4f,%9.4f,%9.4f,%9.4f,%9.4f",
		       Raft, Sector, Bundle, Mirror, 0.1*X, 0.1*Y, 0.1*Z, ThetaZ, DeltaPhi) 
	       << Form(" S: %3i dX %7.4f +/- %6.4f dY %7.4f +/- %6.4f", Status, dX, ddX, dY, ddY)
	       << endl;
  }
  void   Write(ofstream &out) const {
    cout << Form("%2i,%2i,%1i,%1i,%9.4f,%9.4f,%9.4f,%9.4f,%9.4f",
		 Raft, Sector, Bundle, Mirror, 0.1*X, 0.1*Y, 0.1*Z, ThetaZ, DeltaPhi) 
	 << Form(",%3i,%7.4f,%6.4f,%7.4f,%6.4f", Status, dX, ddX, dY, ddY);
  }
};
